def binary_search_problem_51(arr, target):
    # Problem 51: Implement standard binary search to find target in a sorted array.
    low, high = 0, len(arr) - 1
    while low <= high:
        mid = (low + high) // 2
        if arr[mid] == target:
            return mid
        elif arr[mid] < target:
            low = mid + 1
        else:
            high = mid - 1
    return -1

